int CG_get_attr_access( expr, npl, npr )
NODE_T *expr;
NODE_T *npl;
NODE_T *npr;
{
  int i;
  ATREC_T *ap;
  VALUE_T *vp;
  char e_msg[ 256 ];

  if( npr->n_sym == SYM_IDENT )
    return( A_STRUCT );

  vp = &npr->n_val;
  ap = attab;
  for( i = 0; i < n_attab; i++, ap++ ){
    if( !strcmp( ap->a_name, vp->v_value.v_cval ) )
      return(ap->a_access);
  }
  sprintf( e_msg, "unknown attribute '%s'\n", vp->v_value.v_cval );
  expr->n_type = T_ERROR;
  errormsg( FALSE, e_msg );
  return(0);
}

static void checkattr( expr, npl, npr )
NODE_T *expr;
NODE_T *npl;
NODE_T *npr;
{
  int i, found;
  ATREC_T *ap;
  VALUE_T *vp;
  NODE_T *npv;
  SYMREC_T *str, *fld;
  char e_msg[ 256 ];

  if( npl->n_type == T_USER ){
    for( npv = npl; npv; npv = npv->n_left ){
      if( npv->n_sym == SYM_IDENT )
        break;
    }
    str = findsym( npv->n_val.v_value.v_cval );
    if( openusyms( str ) ){
      fld = findsym( npr->n_val.v_value.v_cval );
      if( fld == NULL || fld->s_scope != S_USER ){
        sprintf(e_msg,"fld '%s' not in struct '%s'\n", npr->n_val.v_value.v_cval, npv->n_val.v_value.v_cval );
        errormsg(FALSE,e_msg);
        return;
      }
      expr->n_type = fld->s_type;
      expr->n_class = fld->s_class;
      expr->n_kind = fld->s_kind;
      npr->n_sym = SYM_IDENT;
      closeusyms( NULL );
    }else{
    }
  }else{
    vp = &npr->n_val;
    ap = attab;
    for( found = FALSE, i = 0; i < n_attab; i++, ap++ ){
      if( !strcmp( ap->a_name, vp->v_value.v_cval ) ){
        found = TRUE;
        break;
      }
    }
    if( !found ){
      sprintf( e_msg, "unknown attribute '%s'\n", vp->v_value.v_cval );
      expr->n_type = T_ERROR;
      errormsg( FALSE, e_msg );
      return;
    }
    if( npl->n_class != C_VAR ){
      expr->n_type = T_ERROR;
      errormsg( FALSE,"operator '.': left operand must be a variable.\n" );
      return;
    }
    if( npl->n_kind != K_SCALAR &&
        npl->n_kind != K_DARRAYEL &&
        npl->n_kind != K_HASHEL ){
      expr->n_type = T_ERROR;
      errormsg( FALSE,"operator '.': left operand must be a scalar.\n" );
      return;
    }
    if( !INSET( npl->n_type, ap->a_in ) ){
      expr->n_type = T_ERROR;
      errormsg_s( FALSE,"operator '.': left operand has no attribute '%s'.\n", ap->a_name );
      return;
    }
    expr->n_type = ap->a_type;
    expr->n_class = ap->a_class;
    expr->n_kind = ap->a_kind;
  }
}
